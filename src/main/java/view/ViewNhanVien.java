    private void tbNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbNhanVienMouseClicked
        int row = this.tbNhanVien.getSelectedRow();
        if (row == -1) {
            return;
        }

        String ma = this.tbNhanVien.getValueAt(row, 1).toString();
        String ten = this.tbNhanVien.getValueAt(row, 2).toString();
        String tenDem = this.tbNhanVien.getValueAt(row, 3).toString();
        String ho = this.tbNhanVien.getValueAt(row, 4).toString();
        String gioiTinh = this.tbNhanVien.getValueAt(row, 5).toString();
        String ngaySinh = this.tbNhanVien.getValueAt(row, 6).toString();
        String diaChi = this.tbNhanVien.getValueAt(row, 7).toString();
        String SDT = this.tbNhanVien.getValueAt(row, 8).toString();
        String matKhau = this.tbNhanVien.getValueAt(row, 9).toString();
        String cuaHang = this.tbNhanVien.getValueAt(row, 10).toString();
        String chucVu = this.tbNhanVien.getValueAt(row, 11).toString();
        String nvGuiBC = this.tbNhanVien.getValueAt(row, 12).toString();
        String trangThai = this.tbNhanVien.getValueAt(row, 13).toString();

        txtMa.setText(ma);
        txtTen.setText(ten);
        txtTenDem.setText(tenDem);
        txtHo.setText(ho);
        cbGioiTinh.setSelectedItem(gioiTinh);
        try {
            jdNgaySinh.setDate(new SimpleDateFormat("yyyy-MM-dd").parse(ngaySinh));
        } catch (ParseException ex) {
            System.out.println("Ngay sinh loi: " + ex.getMessage());
        }
        txtDiaChi.setText(diaChi);
        txtSDT.setText(SDT);
        txtMatKhau.setText(matKhau);
        cbCuaHang.setSelectedItem(cuaHang);
        cbChucVu.setSelectedItem(chucVu);
        cbBaoCao.setSelectedItem(nvGuiBC);
        cbTrangThai.setSelectedItem(trangThai);
    }//GEN-LAST:event_tbNhanVienMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        UUID selectedId = UUID.fromString(getSelectedIdFromTable());
        if (selectedId == null) {
            JOptionPane.showMessageDialog(this, "Chưa chọn hàng");
            return;
        }
        if (nhanVienService.delete(selectedId)) {
            JOptionPane.showMessageDialog(this, "Xoá thành công");
        } else {
            JOptionPane.showMessageDialog(this, "Xoá thất bại");
        }
        loadDataTable();
        loadDataBaoCao();
    }//GEN-LAST:event_btnDeleteActionPerformed


    private void loadDataBaoCao() {
        Object[] listNVBC = nhanVienService.findAll().stream().map(nhanVienDTO -> nhanVienDTO.getMa()).toArray();
        cbBaoCao.setModel(new DefaultComboBoxModel(listNVBC));
    }

    private void loadDataCuaHang() {
        Object[] listCuaHang = cuaHangService.findAll().stream().map(cuaHangDTO -> cuaHangDTO.getMa()).toArray();
        cbCuaHang.setModel(new DefaultComboBoxModel(listCuaHang));
    }

    private void loadDataChucVu() {
        Object[] listChucVu = chucVuService.findAll().stream().map(chucVuDTO -> chucVuDTO.getMa()).toArray();
        cbChucVu.setModel(new DefaultComboBoxModel(listChucVu));
    }

    private void loadDataTable() {
        List<NhanVienDTO> nhanVienDTOs = nhanVienService.findAll(currentPage - 1, pageSize);
        DefaultTableModel dtm = (DefaultTableModel) tbNhanVien.getModel();
        tbNhanVien.setModel(dtm);
        dtm.setRowCount(0);
        for (NhanVienDTO nvDTO : nhanVienDTOs) {
            dtm.addRow(new Object[]{
                nvDTO.getId(),
                nvDTO.getMa(),
                nvDTO.getTen(),
                nvDTO.getTenDem(),
                nvDTO.getHo(),
                nvDTO.getGioiTinh(),
                nvDTO.getNgaySinh(),
                nvDTO.getDiaChi(),
                nvDTO.getSdt(),
                nvDTO.getMatKhau(),
                Objects.nonNull(nvDTO.getCuaHang()) ? nvDTO.getCuaHang().getMa() : "",
                Objects.nonNull(nvDTO.getChucVu()) ? nvDTO.getChucVu().getMa() : "",
                Objects.nonNull(nvDTO.getNhanVien()) ? nvDTO.getNhanVien().getMa() : "",
                nvDTO.getTrangThai()
            });
        }
        totalNhanVien = nhanVienService.totalCount();
        lbTotalChucVu.setText("Total: " + totalNhanVien);
        totalPages = (int) (totalNhanVien / pageSize) + 1;
        setStatePagination();
    }

    private void setStatePagination() {
        btnPrevious.setEnabled(currentPage > 1);
        btnNext.setEnabled(currentPage < totalPages);
        lbPagination.setText(currentPage + "/" + totalPages);
    }


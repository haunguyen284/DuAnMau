    private NhanVienDTO getNhanVienFromInput() {
        NhanVienDTO nhanVienDTO = new NhanVienDTO();
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        String tenDem = txtTenDem.getText();
        String ho = txtHo.getText();
        String gioiTinh = cbGioiTinh.getSelectedItem().toString();
        System.out.println(jdNgaySinh.getDate());
        String diaChi = txtDiaChi.getText();
        String SDT = txtSDT.getText();
        String matKhau = txtMatKhau.getText();
        if (Objects.nonNull(cbChucVu.getSelectedItem())) {
            String maChucVu = cbChucVu.getSelectedItem().toString();
            nhanVienDTO.setChucVu(chucVuService.findByMa(maChucVu));
        }
        if (Objects.nonNull(cbCuaHang.getSelectedItem())) {
            String maCuaHang = cbCuaHang.getSelectedItem().toString();
            nhanVienDTO.setCuaHang(cuaHangService.findByMa(maCuaHang));

        }
        if (Objects.nonNull(cbBaoCao.getSelectedItem())) {
            String maNVBC = cbBaoCao.getSelectedItem().toString();
            nhanVienDTO.setNhanVien(nhanVienService.findByMa(maNVBC));
        }
        String trangThai = cbTrangThai.getSelectedItem().toString();
        nhanVienDTO.setMa(ma);
        nhanVienDTO.setTen(ten);
        nhanVienDTO.setTenDem(tenDem);
        nhanVienDTO.setHo(ho);
        nhanVienDTO.setGioiTinh(gioiTinh);
        nhanVienDTO.setNgaySinh(jdNgaySinh.getDate());
        nhanVienDTO.setDiaChi(diaChi);
        nhanVienDTO.setSdt(SDT);
        nhanVienDTO.setMatKhau(matKhau);
        nhanVienDTO.setTrangThai(Integer.parseInt(trangThai));
        return nhanVienDTO;
    }

    private String getSelectedIdFromTable() {
        int selectedRow = tbNhanVien.getSelectedRow();
        if (selectedRow == -1) {
            return null;
        } else {
            return tbNhanVien.getValueAt(selectedRow, 0).toString();
        }
    }


    private void loadDataTable() {
        List<CuaHangDTO> cuaHangDTOs = cuaHangService.findAll(currentPage - 1, pageSize);
        DefaultTableModel dtm = (DefaultTableModel) tbCuaHang.getModel();
        tbCuaHang.setModel(dtm);
        dtm.setRowCount(0);
        for (CuaHangDTO cuaHangDTO : cuaHangDTOs) {
            dtm.addRow(new Object[]{
                cuaHangDTO.getId(),
                cuaHangDTO.getMa(),
                cuaHangDTO.getTen(),
                cuaHangDTO.getDiaChi(),
                cuaHangDTO.getThanhPho(),
                cuaHangDTO.getQuocGia()
            });
        }
        totalChucVu = cuaHangService.totalCount();
        lbTotalChucVu.setText("Total: " + totalChucVu);
        totalPages = (int) (totalChucVu / pageSize) + 1;
        setStatePagination();
    }

    private void setStatePagination() {
        btnPrevious.setEnabled(currentPage > 1);
        btnNext.setEnabled(currentPage < totalPages);
        lbPagination.setText(currentPage + "/" + totalPages);
    }


    private void tbCuaHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbCuaHangMouseClicked
        int row = this.tbCuaHang.getSelectedRow();
        if (row == -1) {
            return;
        }

        String ma = this.tbCuaHang.getValueAt(row, 1).toString();
        String ten = this.tbCuaHang.getValueAt(row, 2).toString();
        String diaChi = this.tbCuaHang.getValueAt(row, 3).toString();
        String thanhPho = this.tbCuaHang.getValueAt(row, 4).toString();
        String quocGia = this.tbCuaHang.getValueAt(row, 5).toString();

        this.txtMa.setText(ma);
        this.txtTen.setText(ten);
        this.txtDiaChi.setText(diaChi);
        this.txtThanhPho.setText(thanhPho);
        this.txtQuocGia.setText(quocGia);
    }//GEN-LAST:event_tbCuaHangMouseClicked

    private void btnPreviousActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPreviousActionPerformed
        if (currentPage > 1) {
            currentPage--;
        }
        loadDataTable();
    }//GEN-LAST:event_btnPreviousActionPerformed

    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        if (currentPage < totalPages) {
            currentPage++;
        }
        loadDataTable();
    }//GEN-LAST:event_btnNextActionPerformed

    private void btnNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewActionPerformed
        CuaHangDTO cuaHangDTO = getCuaHangFromInput();
        String result = cuaHangService.create(cuaHangDTO);
        JOptionPane.showMessageDialog(this, result);
        loadDataTable();
    }//GEN-LAST:event_btnNewActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        CuaHangDTO cuaHangDTO = getCuaHangFromInput();
        String selectedId = getSelectedIdFromTable();
        if (selectedId == null) {
            JOptionPane.showMessageDialog(this, "Chưa chọn hàng");
            return;
        }
        cuaHangDTO.setId(UUID.fromString(selectedId));

        String result = cuaHangService.update(cuaHangDTO);
        JOptionPane.showMessageDialog(this, result);
        loadDataTable();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        UUID selectedId = UUID.fromString(getSelectedIdFromTable());
        if (selectedId == null) {
            JOptionPane.showMessageDialog(this, "Chưa chọn hàng");
            return;
        }
        if (cuaHangService.delete(selectedId)) {
            JOptionPane.showMessageDialog(this, "Xoá thành công");
        } else {
            JOptionPane.showMessageDialog(this, "Xoá thất bại");
        }
        loadDataTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private CuaHangDTO getCuaHangFromInput() {
        CuaHangDTO cuaHangDTO = new CuaHangDTO();
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        String diaChi = txtDiaChi.getText();
        String thanhPho = txtThanhPho.getText();
        String quocGia = txtQuocGia.getText();

        cuaHangDTO.setMa(ma);
        cuaHangDTO.setTen(ten);
        cuaHangDTO.setDiaChi(diaChi);
        cuaHangDTO.setThanhPho(thanhPho);
        cuaHangDTO.setQuocGia(quocGia);

        return cuaHangDTO;
    }

    private String getSelectedIdFromTable() {
        int selectedRow = tbCuaHang.getSelectedRow();
        if (selectedRow == -1) {
            return null;
        } else {
            return tbCuaHang.getValueAt(selectedRow, 0).toString();
        }
    }

